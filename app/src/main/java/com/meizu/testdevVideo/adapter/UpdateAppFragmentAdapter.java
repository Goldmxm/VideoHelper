package com.meizu.testdevVideo.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;
import android.view.ViewGroup;


import java.util.List;

/**
 * Created by maxueming on 2016/12/27.
 */
public class UpdateAppFragmentAdapter extends FragmentPagerAdapter{

    private List<Fragment> fragments;

    public UpdateAppFragmentAdapter(FragmentManager fm, List<Fragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }

    @Override
    public int getCount() {
        return fragments.size();
    }

}
